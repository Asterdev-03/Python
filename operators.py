#Operators

#Arithmetic
num = 10
print(num//3)           #Floor Division -> gives quotient as o/p 
print(num**2)           #Exponent or power


#Relational
print(num>4)
print(num != 5*2)


#Logical
print(num>5 and num<20)
print(not num == 10)


#Bitwise
print(num & 4)          #AND
print(num | 4)          #OR
print(~num)             #NOT
print(num ^ 4)          #XOR
print(num >> 2)         #Right Shift
print(num << 2)         #Left Shift


#Identity
print(type(num) is int)


#Membership
names = ['Hari','Priya','Don']
print('Don' in names)


#Assignment
num **= 3
num //= 15
print(num)
num &= 10
print(num)



